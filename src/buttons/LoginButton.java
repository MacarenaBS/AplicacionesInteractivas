package buttons;
/*==================================================*/
/*=====================Imports======================*/
/*==================================================*/
import java.awt.Font;
import java.awt.event.ActionEvent;
import java.awt.event.ActionListener;
import javax.swing.JButton;
/*==================================================*/
/*===================End Imports====================*/
/*==================================================*/
/**
 * Button to log in to the current application
 * @version 1.0
 * @author ezequiel.de-luca 
 */
public class LoginButton implements ActionListener
{
	/*==================================================*/
	/*====================Variables=====================*/
	/*==================================================*/
	private JButton objButton;
	/*==================================================*/
	/*===================Constructor====================*/
	/*==================================================*/
	/**
	 * @param intXLocation Row Location on the JFrame
	 * @param intYLocation Column Location on the JFrame
	 */
	public LoginButton(int intXLocation, int intYLocation)
	{
		/*==================================================*/
		/*=============Create Password TextBox==============*/
		/*==================================================*/
		this.objButton = new JButton("Login");
		/*==================================================*/
		/*============Set Password TextBox Size=============*/
		/*==================================================*/
		this.objButton.setSize(100, 20);
		/*==================================================*/
		/*============Set Password TextBox Font=============*/
		/*==================================================*/
		this.objButton.setFont(new Font("Arial", Font.PLAIN, 15));
		/*==================================================*/
		/*==========Set Password TextBox Location===========*/
		/*==================================================*/
		this.objButton.setLocation(intXLocation, intYLocation);
		/*==================================================*/
		/*===============Add Action Listener================*/
		/*==================================================*/
		this.objButton.addActionListener(this);
	}
	/*==================================================*/
	/*=================End Constructor==================*/
	/*==================================================*/
	/*==================================================*/
	/*====================Get Button====================*/
	/*==================================================*/
	/**
	 * @return Button Object
	 */
	public JButton getButton()
	{
		/*==================================================*/
		/*==================End Procedure===================*/
		/*==================================================*/
		return this.objButton;
	}
	/*==================================================*/
	/*==================End Procedure===================*/
	/*==================================================*/
	/*==================================================*/
	/*=================Action Listener==================*/
	/*==================================================*/
	@Override
	/**
	 * 
	 */
	public void actionPerformed(ActionEvent objEvent)
	{
	}
	/*==================================================*/
	/*==================End Procedure===================*/
	/*==================================================*/
}
/*==================================================*/
/*====================End Class=====================*/
