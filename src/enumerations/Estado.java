public enum Estado
{
	/*==================================================*/
	/*===================Enumeration====================*/
	/*==================================================*/
	ingresado("ingresado"),
	enTratamiento("enTratamiento"),
	cerrado("cerrado"),
	solucionado("solucionado");
	/*==================================================*/
	/*====================Constants=====================*/
	/*==================================================*/
	private final String strText;
	/*==================================================*/
    /*===================Constructor====================*/
    /*==================================================*/
    private Estado(final String strText)
    {
    	/*==================================================*/
        /*=====================Set Text=====================*/
        /*==================================================*/
        this.strText = strText;
    }
    /*==================================================*/
    /*=================End Constructor==================*/
    /*==================================================*/
    @Override
    /*==================================================*/
    /*================Convert To String=================*/
    /*==================================================*/
    public String toString()
    {
    	/*==================================================*/
        /*==================Return Results==================*/
        /*==================================================*/
        return strText;
    }
    /*==================================================*/
    /*===================End Function===================*/
    /*==================================================*/
}
