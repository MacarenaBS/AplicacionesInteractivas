package model;
/*==================================================*/
/*=====================Imports======================*/
/*==================================================*/
import java.util.Date;
/*==================================================*/
/*===================End Imports====================*/
/*==================================================*/
/**
 * 
 * @author ezequiel.de-luca
 * @version 1.0
 */
public class Accion
{
	/*==================================================*/
	/*====================Variables=====================*/
	/*==================================================*/
	private Date objFecha;
	private String strDescripcion;
	/*==================================================*/
	/*===================Constructor====================*/
	/*==================================================*/
	/**
	 * Crea una nueva acción
	 * @param objFecha
	 * @param strDescripcion
	 */
	public Accion (Date objFecha, String strDescripcion)
	{
		/*==================================================*/
		/*================Establece la fecha================*/
		/*==================================================*/
		this.setFecha(objFecha);
		/*==================================================*/
		/*=============Establece la descripción=============*/
		/*==================================================*/
		this.setDescripcion(strDescripcion);
	}
	/*==================================================*/
	/*=================End Constructor==================*/
	/*==================================================*/
	/*==================================================*/
	/*=========Establece la fecha de la accion==========*/
	/*==================================================*/
	/**
	 * Establece la fecha de la acción
	 * @param objValue
	 */
	private void setFecha(Date objValue)
	{
		/*==================================================*/
		/*================Establece la fecha================*/
		/*==================================================*/
		this.objFecha = objValue;
	}
	/*==================================================*/
	/*===================End Procedure==================*/
	/*==================================================*/
	/*==================================================*/
	/*===Devuelve la fecha de ejecución de la acción====*/
	/*==================================================*/
	/**
	 * Devuelve la fecha de la acción
	 * @return objFecha
	 */
	public Date getFecha()
	{
		/*==================================================*/
		/*================Devuelve la fecha=================*/
		/*==================================================*/
		return this.objFecha;
	}
	/*==================================================*/
	/*===================End Function===================*/
	/*==================================================*/
	/*==================================================*/
	/*======Establece la descripción de la acción=======*/
	/*==================================================*/
	/**
	 * Establece la descripción de la acción realizada
	 * @param strValue
	 */
	private void setDescripcion(String strValue)
	{
		/*==================================================*/
		/*=============Establece la descripción=============*/
		/*==================================================*/
		this.strDescripcion = strValue;
	}
	/*==================================================*/
	/*==================End Procedure===================*/
	/*==================================================*/
	/*==================================================*/
	/*=======Devuelve la descripción de la acción=======*/
	/*==================================================*/
	/**
	 * Devuelve la descripción de la acción realizada
	 * @return
	 */
	public String getDescripcion()
	{
		/*==================================================*/
		/*=============Devuelve la descripción==============*/
		/*==================================================*/
		return this.strDescripcion;
	}
	/*==================================================*/
	/*===================End Function===================*/
	/*==================================================*/
}
