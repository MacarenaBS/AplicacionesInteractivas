package connections;
/**
 * Configuration Parameter Object
 * @version 1.0
 * @author ezequiel.de-luca 
 */
public class ConfigurationParameter
{
	/*==================================================*/
	/*====================Variables=====================*/
	/*==================================================*/
	private String strDescription;
	private String strValue;
	/*==================================================*/
	/*===================Constructor====================*/
	/*==================================================*/
	public ConfigurationParameter(String strDescription, String strValue)
	{
		/*==================================================*/
		/*=================Set Description==================*/
		/*==================================================*/
		this.setDescription(strDescription);
		/*==================================================*/
		/*====================Set Value=====================*/
		/*==================================================*/
		this.setValue(strValue);
	}
	/*==================================================*/
	/*=================End Constructor==================*/
	/*==================================================*/
	/*==================================================*/
	/*=================Get Description==================*/
	/*==================================================*/
	/**
	 * @return Configuration Parameter Description / Name
	 */
	public String getDescription()
	{
		/*==================================================*/
		/*================Return Description================*/
		/*==================================================*/
		return strDescription;
	}
	/*==================================================*/
	/*===================End Function===================*/
	/*==================================================*/
	/*==================================================*/
	/*=================Set Description==================*/
	/*==================================================*/
	/**
	 * Set Configuration Parameter Description
	 * @param strValue Description or Name
	 */
	private void setDescription(String strValue)
	{
		/*==================================================*/
		/*=================Set Description==================*/
		/*==================================================*/
		this.strDescription = strValue;
	}
	/*==================================================*/
	/*==================End Procedure===================*/
	/*==================================================*/
	/*==================================================*/
	/*====================Get Value=====================*/
	/*==================================================*/
	/**
	 * @return Configuration Parameter Value
	 */
	public String getValue()
	{
		/*==================================================*/
		/*===================Return Value===================*/
		/*==================================================*/
		return strValue;
	}
	/*==================================================*/
	/*===================End Function===================*/
	/*==================================================*/
	/*==================================================*/
	/*====================Set Value=====================*/
	/*==================================================*/
	/**
	 * Set Configuration Parameter Value.
	 * @param strValue Value
	 */
	private void setValue(String strValue)
	{
		/*==================================================*/
		/*====================Set Value=====================*/
		/*==================================================*/
		this.strValue = strValue;
	}
	/*==================================================*/
	/*==================End Procedure===================*/
	/*==================================================*/
	/*==================================================*/
	/*======================Equals======================*/
	/*==================================================*/
	/**
	 * Compare the current Configuration Parameter object with the description provided as parameter.
	 * @param objConfigurationParameter Configuration Parameter to compare
	 * @return Configuration Parameter match results
	 */
	public boolean equals(String strParameter)
	{
		/*==================================================*/
		/*=============Return Comparison Result=============*/
		/*==================================================*/
		return this.getDescription().equalsIgnoreCase(strParameter) ? true : false;
	}
	/*==================================================*/
	/*===================End Function===================*/
	/*==================================================*/
}
